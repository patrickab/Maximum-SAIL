"""
For better performance, all MES related
calculations are performed in this module.

Generates Parsec Coordinates to check if sample satisfies constrains
Only such "valid" samples are evaluated and added to the archive.

    - non-intersecting polynomials, 
    - non-negative y-values for upper polynomial
    - more than 1 local optimum for upper polynomial 

Candidate solutions are generated by a custom emitter class, which
adds Gaussian Noise scaled to the boundaries of solution space.

These samples are then evaluated on their
respective target objective function. 

For SAIL these are
    - Acquisition Values
    - Prediction Values

In order to generalize this function for problem domains
different than XFOIL, the generate_parsec_coordinates()
function and the codeblock checking for validity can be
removed.
"""


