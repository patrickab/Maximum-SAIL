"""
Perform MAP-Elites iterations.

IMPORTANT: Make sure to update the target archive before entering MAP-Elites
            Currently, this is done after updating the GP model within eval_xfoil_loop()

Generates Parsec Coordinates to check if sample satisfies constrains.          # non-intersecting polynomials, only non-negative y-values for upper polynomial & only one local optimum for upper polynomial 
Only such "valid" samples are evaluated and added to the archive.

Candidate solutions are generated by a custom emitter class, which
adds Gaussian Noise scaled to the boundaries of solution space.

These samples are then evaluated on their respective objective
function. In the case of SAIL, MAP-Elites is used w/ Acquisitions
& Predictions as objective function.

Newly found elites are added to another archive called
"new_elites_archive". 

This archive can be used for more control over the sampling process

    e.g.: 
    - compare bins in new_elites_archive to bins in obj_archive in
    order to find out which bins are not yet covered by the objective
    - calculate acquisition/prediction improvement compared to obj_elites

In order to generalize this function for problem domains
different than XFOIL, the generate_parsec_coordinates()
function and the codeblock checking for validity can be
removed.
"""


